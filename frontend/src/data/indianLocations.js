// Helper function to get cities for a state
export const getCitiesByState = (state) => {
  return CITIES_BY_STATE[state] || [];
};

// Helper function to search cities across all states
export const searchCities = (query) => {
  if (!query || query.length < 2) return [];
  
  const lowercaseQuery = query.toLowerCase();
  const results = [];
  
  Object.entries(CITIES_BY_STATE).forEach(([state, cities]) => {
    cities.forEach(city => {
      if (city.toLowerCase().includes(lowercaseQuery)) {
        results.push({ city, state });
      }
    });
  });
  
  return results.slice(0, 50); // Limit to 50 results
};
